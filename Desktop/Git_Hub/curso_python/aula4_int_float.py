#Tipos int e Float
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo e Negativo. int sem sinal é considerado
# Positivo.
print(11) #int
print(-11)#int
print(0)
#float -> Número com ponto fluante
#O tipo float representa qualquer número
#Positivo ou negativo com ponto flutuante.
#float sem sinal é considerado positivo.
print(1.1, 10.11) #. ponto separa o número.
print(0.0, -1.5)

#A função type mostra o tipo que o python
#inferiu ao valor.
print( type(12.12), type(-1.8989), type('mam'), type(78))
