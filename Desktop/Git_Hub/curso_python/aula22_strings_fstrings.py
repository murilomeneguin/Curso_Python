'''
S - string
d - int
f - float
.< nÃºmeros de digitos>f
(Caractere) (>< ^)(quantidade)
> - Esquerda
< - Direita
^ - Centro
Sinal - + ou -
Ex.: 0>-100, .f
Conversion flags - !r !s !a
'''
variavel = 'ABC'
print(f'{variavel}')
print(f'{variavel}')
print(f'{variavel:>10}')
print(f'{variavel: <10}.')
print(f'{variavel:^10}.')
print(f'{1000.456546546464546446:1f}')
